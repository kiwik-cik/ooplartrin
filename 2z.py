#Объявляем функцию clicked(), которая будет вызываться при нажатии на кнопку "Приветствие". В этой функции мы изменяем текст метки lab, чтобы отобразить сообщение "Первые и не последние!"
def clicked():
    lab.configure(text='Первые и не последние!')
#Объявляем функцию close_window(), которая будет вызываться при нажатии на кнопку "Закрыть". В этой функции мы вызываем метод destroy() для окна, чтобы закрыть приложение
def close_window():
    window.destroy()
#Создаем объект окна с помощью класса Tk(). Затем устанавливаем заголовок окна на "Проект 2" и задаем размеры окна 400x100 пикселей
window = Tk()
window.title('Проект 2')
window.geometry('400x100')

#Изменение внешнего вида окна
window.configure(bg='lightblue') # Изменение фона окна
#Создаем метку (Label), которая будет отображать текст. Устанавливаем пустой текст и шрифт Arial размером 14. Затем размещаем метку на окне с помощью метода grid()
lab = Label(window, text='', font=('Arial', 14))
lab.grid(column=1, row=0)

