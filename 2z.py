btn = Button(window, text='Приветствие', font=('Arial', 14), command=clicked, width=15, bg='lightgray')
btn.grid(column=0, row=1)
#Аналогично создаем вторую кнопку "Закрыть" и привязываем функцию close_window() к событию нажатия на нее. Затем размещаем кнопку на окне.
btn1 = Button(window, text='Закрыть', font=('Arial', 14), command=close_window, width=15, bg='lightgray')
btn1.grid(column=2, row=1)
#Запускаем главный цикл окна, чтобы приложение начало работать и обрабатывать события пользователя. В этом цикле ожидается взаимодействие пользователя с окном
